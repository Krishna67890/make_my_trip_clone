        // DOM elements
        const destinationsContainer = document.getElementById('destinations-container');
        const regionFilter = document.getElementById('region-filter');
        const typeFilter = document.getElementById('type-filter');
        const ratingFilter = document.getElementById('rating-filter');
        const searchInput = document.getElementById('search');

        // Function to render destination cards
        function renderDestinations(destinations) {
            destinationsContainer.innerHTML = '';
            
            if (destinations.length === 0) {
                destinationsContainer.innerHTML = '<div class="no-results"><i class="fas fa-search"></i><p>No destinations found matching your criteria</p></div>';
                return;
            }
            
            destinations.forEach(destination => {
                const card = document.createElement('div');
                card.className = 'destination-card';
                
                card.innerHTML = `
                    <div class="card-image">
                        <img src="${destination.image}" alt="${destination.name}">
                    </div>
                    <div class="card-content">
                        <h3>${destination.name}</h3>
                        <div class="location">
                            <i class="fas fa-map-marker-alt"></i>
                            <span>${destination.location}</span>
                        </div>
                        <p class="description">${destination.description}</p>
                        <div class="tags">
                            ${destination.tags.map(tag => `<span class="tag">${tag}</span>`).join('')}
                        </div>
                        <p class="best-time">Best time to visit: <span>${destination.bestTime}</span></p>
                        <div class="card-footer">
                            <div class="rating">
                                <i class="fas fa-star"></i>
                                <span>${destination.rating}</span>
                            </div>
                            <button class="view-details">View Details</button>
                        </div>
                    </div>
                `;
                
                destinationsContainer.appendChild(card);
            });
        }

        // Function to filter destinations
        function filterDestinations() {
            const regionValue = regionFilter.value;
            const typeValue = typeFilter.value;
            const ratingValue = parseFloat(ratingFilter.value);
            const searchValue = searchInput.value.toLowerCase();
            
            const filteredDestinations = destinationsData.filter(destination => {
                return (regionValue === 'all' || destination.region === regionValue) &&
                       (typeValue === 'all' || destination.type === typeValue) &&
                       (destination.rating >= ratingValue) &&
                       (destination.name.toLowerCase().includes(searchValue) || 
                        destination.location.toLowerCase().includes(searchValue) ||
                        destination.tags.some(tag => tag.toLowerCase().includes(searchValue)));
            });
            
            renderDestinations(filteredDestinations);
        }

        // Event listeners for filters
        regionFilter.addEventListener('change', filterDestinations);
        typeFilter.addEventListener('change', filterDestinations);
        ratingFilter.addEventListener('change', filterDestinations);
        searchInput.addEventListener('input', filterDestinations);

        // Initial render
        renderDestinations(destinationsData);
